package com.interaction.gesture.Entry;

public class Project {
    String nameString; //软件名称
    String versionString; //版本号
    String publisherString; // 出版商
    String uninstallPathString; //路径
    public Project(String nameString, String versionString, String publisherString, String uninstallPathString) {
        this.nameString = nameString;
        this.versionString = versionString;
        this.publisherString = publisherString;
        this.uninstallPathString = uninstallPathString;
    }

}
